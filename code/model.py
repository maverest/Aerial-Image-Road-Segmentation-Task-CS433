class SimpleCNN(nn.Module):
    def __init__(self, input_channels=3, output_channels=1):
        super(SimpleCNN, self).__init__()

        # First convolutional block
        self.conv1 = nn.Conv2d(in_channels=input_channels, out_channels=32, kernel_size=5, stride=1, padding=2)  # Same padding
        self.pool1 = nn.MaxPool2d(kernel_size=2, stride=2)  # Reduces size to 200x200

        # Second convolutional block
        self.conv2 = nn.Conv2d(in_channels=32, out_channels=64, kernel_size=5, stride=1, padding=2)  # Same padding
        self.pool2 = nn.MaxPool2d(kernel_size=2, stride=2)  # Reduces size to 100x100

        # Upsampling layers to recover spatial dimensions
        self.up1 = nn.ConvTranspose2d(in_channels=64, out_channels=32, kernel_size=2, stride=2)  # Upsample to 200x200
        self.up2 = nn.ConvTranspose2d(in_channels=32, out_channels=output_channels, kernel_size=2, stride=2)  # Upsample to 400x400

    def forward(self, x):
        # Encoder path
        x1 = self.pool1(F.relu(self.conv1(x)))  # Shape: (B, 32, 200, 200)
        x2 = self.pool2(F.relu(self.conv2(x1)))  # Shape: (B, 64, 100, 100)

        # Decoder path (upsampling)
        x3 = F.relu(self.up1(x2))  # Shape: (B, 32, 200, 200)
        x4 = self.up2(x3)  # Shape: (B, output_channels, 400, 400)

        return x4

class DoubleConv(nn.Module):
    def __init__(self, in_channels, out_channels, dropout_rate=0.0):
        super(DoubleConv, self).__init__()
        layers = [
            nn.Conv2d(in_channels, out_channels, 3, 1, 1, bias=False),
            nn.BatchNorm2d(out_channels),
            nn.ReLU(inplace=True),
            #nn.LeakyReLU(0.2, inplace=True),

        ]
        if dropout_rate > 0:
            layers.append(nn.Dropout(dropout_rate))
        layers += [
            nn.Conv2d(out_channels, out_channels, 3, 1, 1, bias=False),
            nn.BatchNorm2d(out_channels),
            nn.ReLU(inplace=True),
            #nn.LeakyReLU(0.2, inplace=True),
        ]
        if dropout_rate > 0:
            layers.append(nn.Dropout(dropout_rate))
        self.conv = nn.Sequential(*layers)

    def forward(self, x):
        return self.conv(x)


